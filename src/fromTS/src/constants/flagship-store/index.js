export const TITLE_IMAGE_RESPONSIVE_STEP = [350, 750];

export const getHardcodeStoreByTagList = (tags) => {
  if (tags?.length) {
    // eslint-disable-next-line no-restricted-syntax
    for (const tag of tags) {
      // eslint-disable-next-line no-restricted-syntax
      for (const flagshipStoreTag of FLAGSHIP_STORE_TAGS) {
        if (tag.includes(flagshipStoreTag)) {
          return MapFlagshipStoreBySlug[flagshipStoreTag];
        }
      }
    }
  }
  return null;
};
